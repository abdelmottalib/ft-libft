#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s2;
	size_t			i;

	s2 = (unsigned char *)s;
	i = 0;
	while (i < n && *(s2 + i) != (unsigned char)c)
		i++;
	if (i == n)
		return (NULL);
	return (s2 + i);
}
